    private void initialize() {
        User user = userList.get(this.rek);
        lblNama.setText(user.getNama() + "!");
        txtNama.setText(user.getNama());
        txtRek.setText(user.getNoRek() + "");
        txtJenis.setText(user.isJenisRek()? "Dewasa":"Kreatif");
                
        formatNum(txtSaldo);
        
        setLocationRelativeTo(null);
        jam();
    }
    
    private void jam(){
        time.setHorizontalAlignment(JLabel.RIGHT);
        tickTock();
        
        Timer timers = new Timer();
        TimerTask tt = new TimerTask(){
            @Override
            public void run() {
                tickTock();
            }
        };
        timers.scheduleAtFixedRate(tt, 1, 1000);
    }
    private void tickTock() {
        time.setText(new SimpleDateFormat("E, d MMM y, HH:mm:ss ").format(new Date()));
    }
    
    private void formatNum(JTextField x) {
        NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
        x.setText(nf.format(userList.get(this.rek).getSaldo()));
    }

