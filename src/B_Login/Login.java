    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        String text = txtRek.getText().trim();
        int rek = Pattern.compile("^\\d\\d$").matcher(text).find()? Integer.valueOf(text):-1;
        User user = rek >= 0 && rek < userList.size()? userList.get(rek):null;
        
        if(user == null)
            JOptionPane.showMessageDialog(this, "Rekening tidak ada", "Warning", 1, null);
        else{
            if(user.cekPIN(String.join("", PIN)))
                new C_Home.Home(this, userList, user.getNoRek()).setVisible(true);
            else
                JOptionPane.showMessageDialog(this, "PIN yang anda masukkan salah", "Warning", 1, null);
        }
    }//GEN-LAST:event_btnEnterActionPerformed

