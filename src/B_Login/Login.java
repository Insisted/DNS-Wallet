    private void txtAngkaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAngkaKeyReleased
        String key = evt.getKeyChar() + "";
        
        if((evt.getKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getKeyCode() == KeyEvent.VK_DELETE) && !PIN.isEmpty() && txtAngka.isEditable()){
            PIN.pop();
        }
        else if(Pattern.compile("\\d").matcher(key).find() && "-‚èé".contains(dropDown.getText())){
            dropDown.setText("‚èé");
            dropDown.setToolTipText("Enter");
            PIN.push(key);
        }
        
        txtAngka.setText(ast());
    }//GEN-LAST:event_txtAngkaKeyReleased

