    private void jam(){
        time.setHorizontalAlignment(JLabel.RIGHT);
        tickTock();
        
        Timer timers = new Timer();
        TimerTask tt = new TimerTask(){
            @Override
            public void run() {
                tickTock();
            }
        };
        timers.scheduleAtFixedRate(tt, 1, 1000);
    }
    private void tickTock() {
        time.setText(new SimpleDateFormat("E, d MMM y, HH:mm:ss ").format(new Date()));
    }
    
    private Stack<String> verify1(){        
        Stack<String> message = new Stack<>();
        String text = txtRek.getText().trim();
        int rek = Pattern.compile("^\\d\\d$").matcher(text).find()? Integer.valueOf(text):-1;
        User user = rek >= 0 && rek < userList.size()? userList.get(rek):null;
        
        if(user == null)
            message.add("Rekening tidak ada");
        else{
            if(!(user.cekPIN(txtPIN.getText().trim()) || user.cekIbu(txtIbu.getText())))
                message.add("Salah Satu Field Harus Diisi dengan Benar");
            if(!Pattern.compile("^\\d{6}$").matcher(PINBaru.getText().trim()).find())
                message.add("PIN Hanya Boleh Mengandung Angka dan Sepanjang 6 Digit");
        }
        message.add(message.isEmpty()? null:"Y");
        
        return message;
    }

