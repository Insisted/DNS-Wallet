    private void btnKonfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonfActionPerformed
        Stack<String> cek = verify1();
        
        if(cek.pop() != null)
            JOptionPane.showMessageDialog(this, String.join("\n", cek), "Warning", 2);
        else{
            int reks = Integer.valueOf(txtRek.getText().trim());
            String baru = PINBaru.getText().trim();
            
            userList.get(reks).gantiPIN(txtPIN.getText().trim(), baru);
            userList.get(reks).gantiPIN(txtIbu.getText().trim(), baru);
            
            JOptionPane.showMessageDialog(this, "PIN Berhasil Diubah", "Berhasil", 1);
            new A_Opening.Opening(this, userList).setVisible(true);
        }
    }//GEN-LAST:event_btnKonfActionPerformed

